import React from "react";

const Modal = ({
  children,
  isOpen,
  onClose,
  title,
  hideHeader,
  showActionBtn,
  actionBtnIcon = null,
  actionBtnText,
  onActionClick,
  showSecondaryActionBtn,
  secondaryActionBtnIcon = null,
  secondaryActionBtnText,
  onSecondaryActionClick,
}) => {
  if (!isOpen) return null;

  return (
    // <div className="fixed inset-0 z-50 flex justify-center items-center w-full h-full bg-black/40">
    //   {/* Modal Content */}
    //   <div
    //     className={`relative flex flex-col bg-[#0B0F19] shadow-lg rounded-lg overflow-hidden 
    //     `}
    //   >
    <div className="fixed inset-0 z-50 flex items-center justify-center bg-black/60">
      <div className="relative bg-[#0E1324] rounded-2xl shadow-xl">
        {/* Modal Header */}
        {!hideHeader && (
          <div className="flex items-center justify-between p-4 border-b border-gray-200">
            <h3 className="md:text-lg font-medium text-white">{title}</h3>

            <div className="flex items-center gap-3 mr-12">
              {showSecondaryActionBtn && (
                <button
                  className="btn-small-light"
                  onClick={() => onSecondaryActionClick()}
                >
                  {secondaryActionBtnIcon}
                  {secondaryActionBtnText}
                </button>
              )}
              
              {showActionBtn && (
                <button
                  className="btn-small-light"
                  onClick={() => onActionClick()}
                >
                  {actionBtnIcon}
                  {actionBtnText}
                </button>
              )}
            </div>
          </div>
        )}

        <button
          type="button"
          className="text-gray-400 bg-transparent hover:bg-gray-200 hover:text-gray-900 rounded-lg text-sm w-8 h-8 flex justify-center items-center absolute top-3.5 right-3.5 cursor-pointer"
          onClick={onClose}
        >
          

          <svg
            className="w-3 h-3"
            aria-hidden="true"
            xmlns="http://www.w3.org/2000/svg"
            
            fill="none"
            viewBox="0 0 14 14"
          >
            <path
              stroke="currentColor"
              strokeLinecap="round"
              strokeLinejoin="round"
              strokeWidth="2"
              d="M1 1l6 6m0 0l6 6M7 7l6-6M7 7l-6 6"
            />
          </svg>
        </button>

        {/* Modal Body (Scrollable) */}
        <div className="flex-1 overflow-y-auto custom-scrollbar">
          {children}
        </div>
      </div>
    </div>
  );
};

export default Modal;
